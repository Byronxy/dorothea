#' @description 
#' The package dorothea contains human and mouse TF regulons. The human regulons 
#'   were curated and collected from different types of evidence such as 
#'   literature curated resources, ChIP-seq peaks, TF binding site motifs and 
#'   interactions inferred directly from gene expression. The mouse regulons 
#'   were constructed by mapping the human gene symbols to their orthologs in 
#'   mice. Those regulons can be coupled with any statistical methodthat aims to 
#'   analyse gene sets to infer TF activity from gene expression data
